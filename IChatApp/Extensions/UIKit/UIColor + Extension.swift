import UIKit

extension UIColor {
    static func buttonRed() -> UIColor {
        return #colorLiteral(red: 0.7307434678, green: 0.1649775803, blue: 0.1949476302, alpha: 1)
    }
    static func mainWhite() -> UIColor {
        return #colorLiteral(red: 0.968627451, green: 0.9725490196, blue: 0.9921568627, alpha: 1)
    }
    static func navigationBarDark() -> UIColor {
        return #colorLiteral(red: 0.1550748348, green: 0.1440279186, blue: 0.1960613728, alpha: 1)
    }
    static func buttonDark() -> UIColor {
        return #colorLiteral(red: 0.2, green: 0.2, blue: 0.2, alpha: 1)
    }
    static func backgroundViewDark() -> UIColor {
        return #colorLiteral(red: 0.2, green: 0.2, blue: 0.2, alpha: 1)
    }
    static func headerTextField() -> UIColor {
        return #colorLiteral(red: 0.608915627, green: 0.6034373641, blue: 0.6296523213, alpha: 1)
    }
    static func headerTextGray() -> UIColor {
        return #colorLiteral(red: 0.5725490196, green: 0.5725490196, blue: 0.5725490196, alpha: 1)
    }
}
